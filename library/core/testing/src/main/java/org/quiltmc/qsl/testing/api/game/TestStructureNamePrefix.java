package org.quiltmc.qsl.testing.api.game;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Specifies that the tests in the annotated class have a global prefix to their structure names.
 */
@Target(ElementType.TYPE)
@Retention(RetentionPolicy.RUNTIME)
public @interface TestStructureNamePrefix {
	/**
	 * {@return the prefix of the structure names used by the tests of the annotated class}
	 */
	String value();
}

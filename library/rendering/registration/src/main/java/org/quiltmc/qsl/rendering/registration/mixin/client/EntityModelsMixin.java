package org.quiltmc.qsl.rendering.registration.mixin.client;

import java.util.Map;

import com.google.common.collect.ImmutableMap;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfoReturnable;
import org.spongepowered.asm.mixin.injection.callback.LocalCapture;

import net.minecraft.client.model.TexturedModelData;
import net.minecraft.client.render.entity.model.EntityModelLayer;
import net.minecraft.client.render.entity.model.EntityModels;

import org.quiltmc.qsl.rendering.registration.impl.client.EntityModelLayerRegistryImpl;

@Mixin(EntityModels.class)
public abstract class EntityModelsMixin {
	@Inject(method = "getModels()Ljava/util/Map;", at = @At(value = "INVOKE", target = "Lcom/google/common/collect/ImmutableMap$Builder;build()Lcom/google/common/collect/ImmutableMap;", remap = false), locals = LocalCapture.CAPTURE_FAILHARD)
	private static void provideCustomModelData(CallbackInfoReturnable<Map<EntityModelLayer, TexturedModelData>> ci, ImmutableMap.Builder<EntityModelLayer, TexturedModelData> builder) {
		EntityModelLayerRegistryImpl.addModelData(builder);
	}
}
